QString CatItem::convertSearchName(const QString& shortName) {
    QString result;// = shortName.toLower();

    for (int i = 0; i<shortName.size(); ++i) {
        // ASCII
        ushort code = shortName[i].unicode();
        if ((code >= 'a' && code <='z') || (code >='A' && code <='Z')) {
            result.append(shortName[i].toLower());
        }
        else if ((code >='0' && code <='9')) {
            result.append(shortName[i]);
        }
        else {
            // zhCN pin yin
            int index = code - 19968;
            if (index >= 0 && index < strChineseFirstPY.length())
                result.append(strChineseFirstPY[index].toLower());
        }

    }
    qDebug() << "CatItem::convertSearchName, shortName:" << shortName
        << ", result:" << result;
    return result;
}

